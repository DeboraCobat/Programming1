package FinalExam;

public class FE_Q1 {

  public static void main(String[] args) {

    int myInteger = 10;
    char myCharacter = 'a';
    double myDouble = 10.1;
    byte myByte = 127;
    short myShort = 10;
    long myLong = 10L;
    boolean myBoolean = true;
    float myFloat = 10.99f;

    System.out.println("This is an integer: " + myInteger);
    System.out.println("This is a char: " + myCharacter);
    System.out.println("This is a double: " + myDouble);
    System.out.println("This is a Byte: " + myByte);
    System.out.println("This is a Short: " + myShort);
    System.out.println("This is a Long: " + myLong);
    System.out.println("This is a Boolean: " + myBoolean);
    System.out.println("This is a Float: " + myFloat);

  }
}