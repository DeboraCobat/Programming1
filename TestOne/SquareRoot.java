import java.lang.Math;

public class SquareRoot {

  public static void main(String args[]) {

    for (int i = 1; i <= 25; i++) {
      double squareroot = Math.pow(i, 2);
      System.out.println("Squareroot of " + i + " is: " + squareroot);

    }

  }
}
